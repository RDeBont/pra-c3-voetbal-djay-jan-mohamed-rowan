            <h2>Wedstrijdreglementen 8 tegen 8 – Groep 7/8</h2>
            <ol>
                <li>
                    <strong>Reglementen</strong><br>
                    De wedstrijden worden gespeeld volgens de reglementen en bepalingen van de KNVB.
                </li>

                <li>
                    <strong>Deelname</strong><br>
                    Speelgerechtigd zijn teams van jongens en meisjes die in groep 7 of 8 zitten en onderwijs volgen op
                    dezelfde
                    school. In overleg met het betreffende district mogen maximaal twee scholen worden samengevoegd tot
                    één
                    schoolvoetbalteam in verband met krimpgebieden. Er mogen 8 spelers opgesteld worden en er zijn 2
                    reservespelers.
                </li>

                <li>
                    <strong>Gemengd spelen</strong><br>
                    Jongens en meisjes mogen in hetzelfde schoolteam spelen. Dergelijke teams spelen mee met teams die
                    volledig
                    uit jongens bestaan.
                </li>

                <li>
                    <strong>Niet wisselen van team</strong><br>
                    Wanneer een school zich heeft ingeschreven met meer dan één team, mogen spelers niet worden
                    overgeheveld van
                    het ene team naar het andere. Dit is ook niet toegestaan wanneer andere teams van dezelfde school
                    zijn
                    uitgeschakeld.
                </li>

                <li>
                    <strong>Ongerechtigde spelers</strong><br>
                    Komt een team met ongerechtigde spelers uit, dan wordt de wedstrijd ongeldig verklaard en wint de
                    tegenpartij met 3-0. Ongerechtigde spelers zijn spelers die:
                    <ul>
                        <li>Niet op de basisschool zitten die het team vertegenwoordigt.</li>
                        <li>Niet tot de groepen behoren die het team vertegenwoordigt.</li>
                        <li>Niet op de vooraf bij de organisatie ingediende spelerslijst staan.</li>
                    </ul>
                </li>

                <li>
                    <strong>Gelijke stand in de poule</strong><br>
                    Wanneer twee of meer ploegen gelijk zijn geëindigd in hun poule, wordt de rangorde bepaald aan de
                    hand van de
                    volgende criteria (in onderstaande volgorde):
                    <ul>
                        <li>Het onderlinge resultaat.</li>
                        <li>Het doelsaldo.</li>
                        <li>Het hoogste aantal gescoorde doelpunten.</li>
                    </ul>
                    Indien deze criteria geen winnaar opleveren, volgt een beslissing door het nemen van strafschoppen.
                </li>

                <li>
                    <strong>Strafschoppen (9 meter)</strong><br>
                    Van elk team nemen om en om vijf verschillende spelers volgens het ABAB-principe een strafschop.
                    Worden
                    evenveel strafschoppen benut, dan nemen beide teams om en om één strafschop totdat er een beslissing
                    valt.
                    Deze extra strafschoppen worden genomen door spelers die nog niet aan de eerste reeks hebben
                    deelgenomen.
                    Hebben alle spelers een strafschop genomen en is er nog geen beslissing, dan mogen ook de eerdere
                    deelnemers opnieuw nemen. Toeschouwers mogen tijdens de strafschoppen het veld niet betreden.
                </li>

                <li>
                    <strong>Wedstrijdduur</strong><br>
                    De wedstrijden duren minimaal 1 x 15 minuten. Er wordt geen time-out gebruikt. Het eerstgenoemde
                    team neemt
                    de aftrap, het andere team kiest welk doel het verdedigt.
                </li>

                <li>
                    <strong>Gelijke stand</strong><br>
                    Staat na een kruisfinale of de finale een gelijke stand op het scorebord, dan volgt een
                    strafschoppenserie
                    zoals omschreven bij regel 7.
                </li>

                <li>
                    <strong>Straftijdregeling</strong><br>
                    Er is een straftijdregeling van 5 minuten van toepassing.
                </li>

                <li>
                    <strong>Uit het veld gestuurd</strong><br>
                    Een speler die door de scheidsrechter uit het veld wordt gestuurd, mag niet deelnemen aan de
                    volgende
                    wedstrijd.
                </li>

                <li>
                    <strong>Spelerslijst</strong><br>
                    Voor aanvang van elk toernooi levert een vertegenwoordiger van de school bij het
                    wedstrijdsecretariaat een
                    spelerslijst in met voor- en achternaam, geboortedatum en geslacht van de deelnemers. Deze lijst
                    geldt ook
                    voor de districtsfinales en de landelijke finale. Het toevoegen van spelers is daarna niet meer
                    toegestaan.
                </li>

                <li>
                    <strong>Kleding</strong><br>
                    Spelers dragen sportkleding. Bij de wedstrijdleiding zijn gekleurde hesjes verkrijgbaar indien twee
                    ploegen
                    dezelfde kleur shirts dragen.
                </li>

                <li>
                    <strong>Niet aanwezig</strong><br>
                    Is een team niet op het veld aanwezig wanneer de wedstrijd moet beginnen, dan wint de tegenpartij
                    met 3-0.
                </li>

                <li>
                    <strong>Leiding</strong><br>
                    De deelnemende scholen zorgen per team voor de aanwezigheid van een wedstrijdleider en begeleider.
                    De
                    wedstrijden in de districtsfinales worden zoveel mogelijk geleid door scheidsrechters uit de
                    KNVB-districten
                    waartoe de deelnemende scholen behoren.
                </li>

                <li>
                    <strong>Wisselen</strong><br>
                    Doorwisselen is toegestaan. Bij 8 tegen 8 mogen maximaal 3 spelers wisselen.
                </li>

                <li>
                    <strong>Balgrootte</strong><br>
                    Er wordt gespeeld met balgrootte nummer 5, met een gewicht van 320 gram.
                </li>

                <li>
                    <strong>Toernooileiding</strong><br>
                    De toernooileiding beslist in gevallen waarin dit reglement niet voorziet.
                </li>
            </ol>

