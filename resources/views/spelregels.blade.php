        <section>
            <h2>Reglementen Paaslijnbal – Basisonderwijs (Groep 7 & 8)</h2>
            <ol>
                <li>De wedstrijden zijn uitsluitend voor leerlingen van groep 7 en 8.</li>
                <li>Een team bestaat uit maximaal 10 spelers, waarvan 8 in het veld staan en 2 mogen indraaien.</li>
                <li>Het eerstgenoemde team begint met de opgooi vanaf achter de stippellijn (ongeveer 1 meter voor de
                    achterlijn).</li>
                <li>Er wordt gespeeld volgens het rallypointsysteem: winnende partij 3 punten, verliezende partij 0
                    punten, bij gelijkspel 1 punt per team.</li>
                <li>Teams dienen 5 minuten voor aanvang bij het speelveld aanwezig te zijn. Te laat komen kan resulteren
                    in een 10-0 verlies.</li>
                <li>Na het fluitsignaal wordt de bal met één hand over het net gegooid. De bal moet in het veld van de
                    tegenpartij komen. Op de lijn is in.</li>
                <li>Een team mag de bal maximaal 3 keer aanraken voordat deze naar de tegenpartij wordt gespeeld.</li>
                <li>Een speelster mag de bal slechts éénmaal achter elkaar aanraken (dubbel vangen of navangen is niet
                    toegestaan).</li>
                <li>De bal mag met elk lichaamsdeel tot navelhoogte worden aangeraakt.</li>
                <li>Twee speelsters mogen de bal gelijktijdig aanraken (telt als twee keer spelen voor het team).</li>
                <li>De bal mag het net raken, ook bij opgooien.</li>
                <li>Een speelster mag het net of de middellijn niet aanraken.</li>
                <li>De plaats van vangen is de plaats van werpen. Lopen met de bal is niet toegestaan.</li>
                <li>Alleen netspelers mogen de bal opspringend over het net spelen.</li>
                <li>Begin en einde van de wedstrijd wordt centraal aangegeven.</li>
                <li>Finalewedstrijden: rally doorspelen na eindsignaal, verschil van minimaal 2 punten verplicht.</li>
                <li>Nethoogte: 205 cm.</li>
                <li>Duur van de wedstrijden: 10 minuten.</li>
                <li>Bij gelijke eindstand: onderlinge uitslag, meeste punten voor, minste punten tegen, tenslotte
                    loting.</li>
            </ol>
        </section>

