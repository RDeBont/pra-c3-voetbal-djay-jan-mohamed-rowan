            <h2>Reglementen Paaslijnbal Basisonderwijs</h2>
            <ol>
                <li>De wedstrijden zijn uitsluitend voor leerlingen van groep 7 en 8 van de basisschool.</li>
                <li>Een spelend lijnbalteam bestaat uit maximaal 10 spelers. Hiervan staan er 8 in het veld; 2
                    speelsters
                    mogen indraaien.</li>
                <li>Het eerstgenoemde team begint met de opgooi.</li>
                <li>De opgooi mag gebeuren vanaf een willekeurig punt achter de stippellijn (ongeveer 1 meter voor de
                    achterlijn).</li>
                <li>Er wordt gespeeld volgens het rallypointsysteem: winst = 3 punten, verlies = 0 punten, gelijkspel =
                    beide
                    teams 1 punt.</li>
                <li>Teams dienen 5 minuten voor aanvang bij het betreffende veld aanwezig te zijn. Kan een wedstrijd
                    niet op
                    tijd beginnen of niet gespeeld worden, dan wordt het team dat niet aanwezig is geacht met 10-0
                    verloren te
                    hebben.</li>
                <li>Na het fluitsignaal wordt de bal met één hand over het net gegooid. De bal moet in het veld van de
                    tegenpartij komen. Op de lijn is in.</li>
                <li>Elk team mag de bal maximaal 3 keer aanraken voordat deze naar de tegenpartij wordt gespeeld.</li>
                <li>Een speelster mag de bal slechts 1 keer achter elkaar aanraken. Dubbel vangen of navangen is niet
                    toegestaan. De eerste vangster mag wel opnieuw aangespeeld worden.</li>
                <li>De bal mag (met inachtneming van punt 8) met elk lichaamsdeel tot navelhoogte worden aangeraakt.
                </li>
                <li>Twee speelsters mogen de bal gelijktijdig aanraken. Dit geldt als 2 keer spelen.</li>
                <li>De bal mag tijdens het spelen het net raken, ook bij het opgooien.</li>
                <li>Een speelster mag het net niet aanraken.</li>
                <li>Een speelster mag niet over de middellijn onder het net door de andere speelhelft aanraken. De voet
                    moet
                    wel helemaal over de lijn zijn.</li>
                <li>De plaats van vangen is ook de plaats van werpen/gooien. Lopen met de bal of een stap zetten is niet
                    toegestaan. Gooien in de sprong mag wel, maar niet eerst met een klein hupsje “goed gaan staan”.
                </li>
                <li>Zowel in het 4-4 systeem als in het 3-2-3 systeem mogen alleen de netspelers de bal opspringend over
                    het
                    net spelen.</li>
                <li>Blokkeren is niet toegestaan.</li>
                <li>Begin en einde van de wedstrijd worden centraal aangegeven.</li>
                <li>Bij finalewedstrijden wordt na het eindsignaal de lopende rally uitgespeeld en moet er een verschil
                    van 2
                    punten zijn.</li>
                <li>De hoogte van het net is 205 cm.</li>
                <li>De wedstrijden duren 10 minuten.</li>
                <li>Finalewedstrijden moeten altijd eindigen met minimaal 2 punten verschil.</li>
                <li>Bij gelijke eindstand gelden de volgende regels:
                    <ol type="a">
                        <li>De uitslag van de onderlinge wedstrijd.</li>
                        <li>De meeste punten voor.</li>
                        <li>De minste punten tegen.</li>
                        <li>Tossen.</li>
                    </ol>
                </li>
            </ol>
